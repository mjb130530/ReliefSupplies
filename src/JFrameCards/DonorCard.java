    private String[] getLocationsForDropdown(){
        LocationDAO locationDAO = new LocationDAOImpl(); 
        ArrayList<Location> listOfLocations = new ArrayList<>();
        try {
            listOfLocations = locationDAO.retrieveAllLocations(getConnection());
            }
        catch (SQLException | IOException ex) {
            Logger.getLogger(DonorCard.class.getName()).log(Level.SEVERE, null, ex);
        }
        String[] locationList = new String[listOfLocations.size()];
        for(int i = 0; i < listOfLocations.size(); i++) {
            locationList[i] = "" + listOfLocations.get(i).getCity() + ", " + listOfLocations.get(i).getLocationState() + "";
        }
        return locationList;
        
    }
    
    private Connection getConnection() throws SQLException, IOException{
        DataSource dataSource = DBConnection.getDataSource();
        Connection connection = dataSource.getConnection();
        //connection.setAutoCommit(false);
        return connection;
    }

